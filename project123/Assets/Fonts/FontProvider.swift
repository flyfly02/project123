import Foundation
import UIKit



extension UIFont {
    static func nunitoExtraBold(size: CGFloat = 14) -> UIFont? {
        .init(name: "Nunito-ExtraBold", size: size)
    }
    
    static func nunitoSemiBold(size: CGFloat = 14) -> UIFont? {
        .init(name: "Nunito-SemiBold", size: size)
    }
}

